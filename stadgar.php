<p>Antagen på årsmötet 2020-02-11</p>
<p>Organisationsnummer 802467-4684</p>
<h3>§1 Organisation och Syfte</h3>
<p>mom. 1 Föreningens juridiska namn är Viktor Rydberg Odenplans Elevkår.</p>
<p>mom. 2 Viktor Rydberg Odenplans Elevkårs syfte är att tillvarata sina medlemmars</p>
<p>ekonomiska, ideella, sociala och fackliga intressen.</p>
<p>mom. 3 Viktor Rydberg Odenplans Elevkår är partipolitiskt, ideologiskt och religiöst</p>
<p>obunden.</p>
<p>mom. 4 Viktor Rydberg Odenplans Elevkår har sitt säte i Stockholm.</p>
<p>mom. 5 Viktor Rydberg Odenplans Elevkårs organisation ska styras i enlighet med</p>
<p>reglementet.</p>
<p>mom. 6 För att ändra i reglementet krävs enkel majoritet vid omröstning på årsmötet.</p>
<h3>§2 Medlemskap</h3>
<p>mom. 1 Alla elever på Viktor Rydberg Gymnasium Odenplan har rätt att bli medlemmar i</p>
<p>Viktor Rydberg Odenplans Elevkår.</p>
<p>mom. 2 Viktor Rydberg Odenplans Elevkårs målsättning är att alla elever på skolan ska</p>
<p>vara medlemmar i Viktor Rydberg Odenplans Elevkår.</p>
<p>mom. 3 Medlemsavgiften för Viktor Rydberg Odenplans Elevkår bestäms av årsmötet.</p>
<p>mom. 4 En elev på Viktor Rydberg Odenplan blir medlem i Viktor Rydberg Odenplans</p>
<p>elevkår genom att, med sitt godkännande, registreras i Sveriges Elevkårers databas.</p>
<p>mom. 5 Ett medlemskap gäller till kalenderårets slut.</p>
<p>mom. 6 Medlemskapet avslutas omedelbart om:</p>
<ul>
<li>Medlemmen ansöker om utträde.</li>
<li>Medlemmen blir utesluten ur Viktor Rydberg Odenplans Elevkår.</li>
</ul>
<h3>§3 Årsmötet</h3>
<p>mom. 1 Årsmötet är Viktor Rydberg Odenplans Elevkårs högst beslutande organ.</p>
<p>mom. 2 Årsmötet ska hållas under perioden februari till och med mars varje år.</p>
<p>mom. 3 Styrelsen ska kalla medlemmarna till årsmötet minst sex (6) veckor innan</p>
<p>årsmötet äger rum. Kallelsen ska göras genom affischer i skolan samt utskick till</p>
<p>medlemmarnas skolmejl. Kallelsen ska innehålla tid, plats, datum för årsmötet,</p>
<p>propositioner inför årsmötet samt motions- och nomineringsstopp.</p>
<p>mom. 4 Motionsstopp är fyra (4) veckor före årsmötet.</p>
<p>mom. 5 Alla handlingar, det vill säga åtminstone</p>
<ul>
<li>Motioner och motionssvar</li>
<li>Verksamhetsberättelse</li>
<li>Bokslut</li>
<li>Valberedningsförslag ska finnas tillgängliga för medlemmarna minst två (2) veckor före årsmötet</li>
</ul>
<p>mom. 6 Alla medlemmar i Viktor Rydberg Odenplans Elevkår har yttranderätt,</p>
<p>förslagsrätt och rösträtt på årsmötet. Revisorer och valberedning för Viktor Rydberg</p>
<p>Odenplans Elevkår har närvarorätt, yttranderätt och förslagsrätt på årsmötet.</p>
<p>mom. 7 Åtminstone dessa punkter ska behandlas under årsmötet:</p>
<ul>
<li>Mötets öppnande.</li>
<li>Val av ordförande, sekreterare, justerare och rösträknare för årsmötet.</li>
</ul>
<p>Rösträknare får inte vara medlem i styrelsen.</p>
<ul>
<li>Fastställande av röstlängd</li>
<li>Fråga om mötet blivit utlyst enligt stadgarna.</li>
<li>Fastställande av dagordning</li>
<li>Presentation och godkännande av verksamhetsberättelse och bokslut.</li>
<li>Fråga om ansvarsfrihet för den avgående styrelsen.</li>
<li>Behandling av propositioner och motioner</li>
<li>Val av styrelse</li>
</ul>
<p>- Ordförande</p>
<p>- Vice ordförande</p>
<p>- Skattmästare</p>
<p>- Utskottsansvarig</p>
<p>- Administratör</p>
<p>- Kommittéansvarig</p>
<p>- Projektansvarig</p>
<ul>
<li>Val av utskottsgruppen</li>
</ul>
<p>- Ordförande för traditionsutskottet</p>
<p>- Ordförande för finansiella utskottet</p>
<p>- Ordförande för sociala utskottet</p>
<p>- Ordförande för kommunikationsutskottet</p>
<p>- Ordförande för påverkansutskottet</p>
<p>- Ordförande för miljöutskottet</p>
<p>- Ordförande för idrottsutskottet</p>
<p>- Ordförande för studentutskottet</p>
<ul>
<li>Val av valberedning och revisorer</li>
<li>Val av styrelsesuppleant</li>
<li>Årsmötets avslutande</li>
</ul>
<p>mom. 8 Verksamhetsåret kallas tiden mellan två årsmöten. Räkenskapsåret är 1</p>
<p>januari till 31 december.</p>
<p>mom. 9 Alla handlingar samt mötesprotokoll från årsmötet ska vara justerade och</p>
<p>finnas tillgängliga för medlemmarna senast fyra veckor efter årsmötets slut.</p>
<p>mom. 10 Ett extra årsmöte ska hållas om styrelsen eller en majoritet (mer än hälften) av</p>
<p>medlemmarna begär det.</p>
<p>mom. 11 För det extra årsmötet gäller motsvarande stadgar som för det ordinarie</p>
<p>årsmötet, med enda skillnaden att det extra årsmötet kan hållas när som helst under</p>
<p>året.</p>
<h3>§4 Styrelsen</h3>
<p>mom. 1 Styrelsen är ansvarig för Viktor Rydberg Odenplans Elevkårs förvaltning och</p>
<p>verksamhet mellan årsmötena. Styrelsen träder i kraft efter årsmötet.</p>
<p>mom. 2 Styrelsen väljs av årsmötet och består av en ordförande och ett jämnt antal</p>
<p>ledamöter. Styrelsen måste minst innefatta posterna ordförande, vice ordförande,</p>
<p>skattmästare, utskottsansvarig, administratör, kommittéansvarig och projektansvarig.</p>
<p>mom. 3 Styrelsens uppgifter är att:</p>
<ul>
<li>Se till att årsmötets beslut verkställs</li>
<li>Se till att stadgarna följs</li>
<li>Förbereda nästa års årsmöte och lägga fram verksamhetsberättelse och bokslut</li>
</ul>
<p>samt proposition på verksamhetsplan till årsmötet</p>
<ul>
<li>Sköta Viktor Rydberg Odenplans Elevkårs ekonomi</li>
<li>Tillvarata sina medlemmars ekonomiska, ideella, sociala och fackliga intressen.</li>
</ul>
<p>mom. 4 Styrelsen sammanträder ifall ordförande eller en majoritet av ledamöterna</p>
<p>sammankallar. Styrelsen kan fatta beslut om minst hälften av styrelsemedlemmarna är</p>
<p>närvarande.</p>
<p>mom. 5 Om ordföranden avgår, tar vice ordföranden över dennes post. Styrelsen väljer</p>
<p>då inom sig en ny vice ordförande. Om en annan styrelseledamot avgår, ersätts denna</p>
<p>av en av årsmötet vald styrelsesuppleant.</p>
<p>mom. 6 Tiden efter årsmötet sitter den avgående och tillträdande styrelsen parallellt</p>
<p>under en överlämningsperiod som sträcker sig fram till och med att femton (15)</p>
<p>ordinarie skoldagar har passerat. Den avgående styrelsen har bara närvaro- och</p>
<p>yttranderätt under överlämningsperioden.</p>
<h3>§5 Utskottsgruppen</h3>
<p>mom. 1 Utskottsgruppen väljs av årsmötet och består av utskottsansvarig, vald till</p>
<p>styrelsen, och ett jämnt antal ledamöter. Utskottsgruppen träder i kraft efter årsmötet,</p>
<p>med undantag för ordförande för studentutskottet som träder i kraft höstterminen efter</p>
<p>årsmötet.</p>
<p>mom. 2 Utskottsgruppen måste minst innefatta posterna utskottsansvarig, ordförande</p>
<p>för traditionsutskottet, ordförande för finansiella utskottet, ordförande för sociala</p>
<p>utskottet, ordförande för kommunikationsutskottet, ordförande för påverkansutskottet,</p>
<p>ordförande för miljöutskottet, ordförande för idrottsutskottet och ordförande för</p>
<p>studentutskottet.</p>
<p>mom. 3 Utskottsgruppens uppgifter är att:</p>
<ul>
<li>Främja samarbete mellan utskotten.</li>
<li>Föra en effektiv kommunikation mellan styrelsen och utskotten.</li>
<li>Fördela ansvar för den operativa verksamheten mellan utskotten.</li>
</ul>
<p>mom. 4 Utskottsgruppen sammanträder ifall utskottsansvarige eller en majoritet av</p>
<p>ledamöterna sammankallar. Utskottsgruppen kan fatta beslut om minst hälften av</p>
<p>ledamöterna är närvarande.</p>
<p>mom. 5 Samtliga ledamöter, samt utskottsansvarige har yttranderätt, förslagsrätt och</p>
<p>rösträtt.</p>
<p>mom. 6 Tiden efter årsmötet sitter den avgående och tillträdande utskottsgruppen</p>
<p>parallellt under en överlämningsperiod som sträcker sig fram till och med femton (15)</p>
<p>ordinarie skoldagar har passerat. Den avgående utskottsgruppen har bara närvarooch yttranderätt under överlämningsperioden.</p>
<h3>§6 Valberedningen</h3>
<p>mom. 1 Valberedningen består av en ordförande och ett jämnt antal ledamöter.</p>
<p>mom. 2 Valberedningens uppgift är att lägga fram ett förslag till årsmötet inför val av</p>
<p>styrelse, utskottsgrupp, revisorer och valberedning.</p>
<p>mom. 3 Valberedningen har rätt att närvara vid all verksamhet Viktor Rydberg</p>
<p>Odenplans Elevkår anordnar.</p>
<h3>§7 Revision</h3>
<p>mom. 1 Revisorerna består av ett ojämnt antal ledamöter.</p>
<p>mom. 2 Revisorernas uppgift är att:</p>
<ul>
<li>Granska huruvida styrelsen följer stadgan, samt granska styrelsens verksamhet,</li>
</ul>
<p>ekonomiska berättelse och hantering av Viktor Rydberg Odenplans Elevkårs</p>
<p>räkenskaper.</p>
<ul>
<li>Avlämna en revisionsberättelse senast två veckor före årsmötet.</li>
<li>Att i revisionsberättelsen ta upp hur de anser att styrelsen skött sina uppgifter i</li>
</ul>
<p>enlighet med stadgarna, samt avgöra huruvida den avgående styrelsen bör beviljas</p>
<p>ansvarsfrihet.</p>
<p>mom. 3 Revisorerna har rätt att närvara vid all verksamhet Viktor Rydberg Odenplans</p>
<p>Elevkår anordnar samt vid styrelsemötena.</p>
<p>mom. 4 Revisorerna får inte sitta i valberedningen eller styrelsen inom Viktor Rydberg</p>
<p>Odenplans Elevkår.</p>
<h3>§8 Utskotten</h3>
<p>mom. 1 Vice ordförande inom ett utskott, som blivit vald på ett demokratiskt sätt inom</p>
<p>utskottet, tillträder som ny ordförande för utskottet om den förtroendevalda</p>
<p>ordföranden av någon anledning måste avgå. Denne ska då ta vid alla de uppdrag som</p>
<p>ordföranden lämnat, alltså leda utskottet och i fortsättningen vara en del av</p>
<p>utskottsgruppen. Det väljs även en ny vice ordförande inom utskottet efter att den</p>
<p>tidigare vice ordföranden tillträtt som ordförande.</p>
<p>mom. 2 Utskotten jobbar inom olika områden och respektive utskotts syfte lyder enligt</p>
<p>följande:</p>
<h4>Traditionsutskottet</h4>
<p>Utskottets syfte är att främja firanden av olika slag kopplade till traditioner. Utskotten</p>
<p>firar de, befintliga såväl som nya, traditioner som anses relevanta för elevkårens</p>
<p>medlemmar och uppmärksammar dessa på lämpligt sätt.</p>
<h4>Sociala utskottet</h4>
<p>Utskottets syfte är att främja den sociala andan och gemenskapen på skolan. Utskottet</p>
<p>är ansvariga för klasspokalen och anordnar aktiviteter kopplade till den samt andra</p>
<p>aktiviteter vars syfte är att skapa en god social gemenskap på skolan.</p>
<h4>Påverkansutskottet</h4>
<p>Utskottets syfte är att samla och lyfta medlemmars frågor och åsikter som rör deras</p>
<p>skoltid, genom bland annat klassråden. Utskottet lobbar mot skolan och andra aktörer</p>
<p>utifrån medlemmarnas åsikter.</p>
<h4>Miljöutskottet</h4>
<p>Utskottet har som uppdrag att lyfta de miljöfrågor som anses relevanta för medlemmar</p>
<p>och i möjlig mån verka för en mer miljövänlig skoltid. Utskottet ska även arbeta med</p>
<p>bildning kring miljöfrågor.</p>
<h4>Finansiella utskottet</h4>
<p>Utskottets syfte är att ge medlemmarna olika typer av service, framför allt ekonomiska</p>
<p>förmåner. Utskottet jobbar med kårkorten, rabatter i närområdet, sponsring till</p>
<p>aktiviteter och event inom kårens verksamhet, samt eventuella samarbeten med</p>
<p>externa parter som anses relevanta.</p>
<h4>Kommunikationsutskottet</h4>
<p>Utskottets syfte är att ge medlemmarna den information som behövs för att de ska</p>
<p>kunna ta del av elevkårens verksamhet. Utskottet har huvudansvar för elevkårens alla</p>
<p>sociala kanaler och skapar det innehåll som finns där, samt marknadsföringen i skolan</p>
<p>via anslagstavlan.</p>
<h4>Idrottsutskottet</h4>
<p>Utskottets syfte är att erbjuda olika fysiska aktiviteter till medlemmarna. Utskottet</p>
<p>verkar även för en god, grön och aktiv laganda på skolan som stärker gemenskapen</p>
<p>och bidrar till en tävlingsanda, mot exempelvis andra skolor. Utskottet samarbetar med</p>
<p>relevanta externa parter, som exempelvis andra föreningar med liknande syfte, på</p>
<p>skolan och på andra skolor.</p>
<h4>Studentutskottet</h4>
<p>Utskottets syfte är att skapa en minnesvärd tid för medlemmarna i årskurs tre som</p>
<p>blivande studenter. Utskottet jobbar främst med de aktiviteter som hör studenttiden till</p>
<p>både redan befintliga aktiviteter som balen och slutskivan, och vid eventuell efterfrågan</p>
<p>nya aktiviteter.</p>
<h3>§9 Omröstning</h3>
<p>mom. 1 Enkel majoritet råder vid omröstning. Alltså krävs mer än hälften av rösterna för</p>
<p>att ett förslag ska vinna. Väljer någon att inte rösta, det vill säga att lägga ned sin röst i</p>
<p>en fråga så räknas inte denna.</p>
<p>mom. 2 Om fler förslag står men inget får enkel majoritet genomförs en omröstning med</p>
<p>de två förslag med flest röster.</p>
<p>mom. 3 Omröstning sker i första hand genom acklamation (ja-rop) och i andra hand</p>
<p>genom votering (handuppräckning) om det begärs. I tredje hand räknas rösterna om</p>
<p>rösträkning begärs.</p>
<p>mom. 4 Vid lika röstetal skall en sekundär omröstning ta plats. Vid lika röstetal i den</p>
<p>sekundära omröstningen vinner den mening som organets ordförande eller dennes</p>
<p>ersättare förespråkar. Detta gäller inte vid årsmötet. Ifall denne avstår från att ta det</p>
<p>beslutet, eller inte finns på plats, avgörs omröstningen genom lottning. Detta gäller dock</p>
<p>inte vid personval.</p>
<p>mom. 5 Vid personval gäller att:</p>
<ul>
<li>Varje person som ska väljas måste få egen majoritet, alltså mer än hälften av rösterna.</li>
<li>Om inte tillräckligt många personer får egen majoritet ska resten av platserna tillsättas</li>
</ul>
<p>med en andra valomgång, där egen majoritet inte är nödvändig.</p>
<ul>
<li>Om flera personer får lika många röster på ett sätt som kan påverka resultatet av</li>
</ul>
<p>omröstningen, ska en skiljeomröstning genomföras mellan dessa.</p>
<ul>
<li>Vid lika röstetal i skiljeomröstningen ska lottning ske.</li>
</ul>
<p>mom. 6 En person med rösträtt kan inte överlåta sin röst till någon annan. Det är inte</p>
<p>tillåtet att rösta med fullmakt.</p>
<h3>§10 Valbarhet</h3>
<p>mom. 1 Alla Viktor Rydberg Odenplans Elevkårs medlemmar som, enligt stadgan, kan</p>
<p>förbli medlemmar under kommande verksamhetsår är valbara till samtliga uppdrag</p>
<p>inom Viktor Rydberg Odenplans Elevkår, undantaget ordförande för studentutskottet,</p>
<p>vilket enbart medlemmar i årskurs 2 kan väljas till.</p>
<p>mom. 2 Till ledamöter i valberedningen och revisionen är, förutom Viktor Rydberg</p>
<p>Odenplans Elevkårs medlemmar, även personer som inte är medlemmar, eller de som</p>
<p>enligt stadga inte kan förbli medlemmar under kommande verksamhetsår i Viktor</p>
<p>Rydberg Odenplans Elevkår valbara.</p>
<h3>§11 Firmateckning</h3>
<p>mom. 1 Styrelsen ska inom sig utse två personer till att teckna föreningens firma.</p>
<p>mom. 2 Styrelsen kan även utse medlemmar utanför styrelsen till att teckna föreningens</p>
<p>firma.</p>
<h3>§12 Uteslutning</h3>
<p>mom. 1 Endast årsmötet kan utesluta en medlem i Viktor Rydberg Odenplans Elevkår</p>
<p>ifall denne motverkat Viktor Rydberg Odenplans Elevkårs syfte som det kommer till</p>
<p>uttryck i stadgan eller på annat sätt allvarligt skadat Viktor Rydberg Odenplans Elevkår.</p>
<p>mom. 2 Beslutet måste tas med minst två tredjedelars majoritet.</p>
<h3>§13 Tolkning av stadgan</h3>
<p>mom. 1 Om det råder delade meningar om hur stadgarna ska tolkas är det styrelsens</p>
<p>tolkning som gäller.</p>
<p>mom. 2 Beslutet om tolkning gäller fram till nästkommande årsmöte, där frågan ska</p>
<p>avgöras.</p>
<h3>§14 Ändring av stadgan</h3>
<p>mom. 1 För att ändra i stadgan krävs två tredjedelars majoritet på ett årsmöte eller</p>
<p>enkel majoritet på två på varandra följande årsmöten</p>
<h3>§15 Upplösning</h3>
<p>mom. 1 För att Viktor Rydberg Odenplans Elevkår ska kunna upplösas måste det</p>
<p>föreslås skriftligen i en motion till ett årsmöte.</p>
<p>mom. 2 Om ett årsmöte beslutar att upplösa Viktor Rydberg Odenplans Elevkår ska</p>
<p>samma årsmöte besluta vad som händer med Viktor Rydberg Odenplans Elevkårs</p>
<p>resurser.</p>
<p>mom. 3 Beslut om upplösning måste tas med minst två tredjedelars majoritet, på två på</p>
<p>varandra följande årsmöten</p>
