<p>Antagen på årsmötet 2020-02-11</p>
<p>Organisationsnummer 802467-4684</p>
<h3>§ 1 Elevkåren</h3>
<p>Mom. 1 Elevkåren är en enskild organisation helt fristående från skolan.</p>
<p>Mom. 2 Elevkåren är en förening bildad av elever för elever.</p>
<p>Mom. 3 Elevkåren syftar till att göra skolgången mer givande för sina medlemmar.</p>
<p>Mom. 4 Samtliga medlemmar i elevkåren har alla rätt att ta del av samtlig verksamhet</p>
<p>och samtliga förmåner som tillskrivs dem av styrelsen.</p>
<h3>§ 2 Elevkårsstyrelsen</h3>
<p>Mom. 1 Det åligger elevkårsstyrelsen att arbeta för att tillgodose medlemmarnas</p>
<p>önskemål och förfrågningar.</p>
<p>Mom. 2 Elevkårsstyrelsens arbete leds av ordföranden.</p>
<p>Mom. 3 Elevkårsstyrelsen skall inom sig jämnt uppdela ansvaret för kårens förvaltning,</p>
<p>ekonomi och verksamhet.</p>
<p>Mom. 4 Utskottsansvarige är ansvarig för att leda utskottsgruppens arbete</p>
<h3>§ 3 Utskott</h3>
<p>Mom. 1 Ett utskott tillsätts på initiativ av elevkårsstyrelsen eller en medlem och skall</p>
<p>arbeta med en specifik typ av verksamhet.</p>
<p>Mom. 2 Ett utskott består till dess att elevkårsstyrelsen eller utskottet själv beslutar att</p>
<p>lösa upp det.</p>
<p>Mom. 3 Ett utskott ska ledas av en ordförande som är ytterst ansvarig för utskottets</p>
<p>verksamhet inför elevkårsstyrelsen.</p>
<p>Mom. 4 De utskotten vars ansvarsområde faller under utskottsgruppen leds av</p>
<p>respektive ledamot för ansvarsområdena</p>
<p>Mom. 5 Inom sig ska varje utskott utse en vice ordförande och en sekreterare som för</p>
<p>mötesprotokoll. Sekreterarposten kan vara vandrande.</p>
<p>Mom. 6 Ett utskott skall kunna redovisa sin verksamhet för elevkårsstyrelsen på</p>
<p>begäran.</p>
<p>Mom. 7 Ett utskott har rätt att ansöka om pengar från elevkårsstyrelsen till sin</p>
<p>verksamhet. Det är upp till elevkårsstyrelsen att antingen bevilja eller avslå denna</p>
<p>ansökan.</p>
<p>Mom. 8 Respektive utskottsordförande beslutar i samråd med utskottsansvarig antalet</p>
<p>ledamöter samt vilka som ska sitta i utskottet utifrån inkommande ansökningar</p>
<p>Mom. 9 Utskotten tillsätts i september varje år och sitter läsåret ut.</p>
<h3>§ 4 Kommittéer</h3>
<p>Mom. 1 Alla medlemmar har rätt att ansöka om att få starta en kommitté.</p>
<p>Mom. 2 Det är upp till elevkårsstyrelsen att bevilja eller avslå denna ansökan. En</p>
<p>kommittés syfte eller verksamhet får inte gå emot elevkårens stadga, styrdokument</p>
<p>eller övriga beslut.</p>
<p>Mom. 3 En kommitté får arbeta med valfri typ av verksamhet så länge stadga,</p>
<p>reglemente och andra styrdokument följs.</p>
<p>Mom. 4 En kommittés arbete ska ledas av en ordförande som blir ytterst ansvarig för</p>
<p>verksamheten.</p>
<p>Mom. 5 En kommitté och dess verksamhet skall stå öppen för alla medlemmar.</p>
<p>Mom. 6 En kommitté ska ha regelbundna möten och ska på begäran kunna redovisa sin</p>
<p>verksamhet för elevkårsstyrelsen.</p>
<p>Mom. 7 En kommitté har rätt att ansöka om pengar från elevkårsstyrelsen. Det är upp till</p>
<p>elevkårsstyrelsen att antingen bevilja eller avslå denna ansökan.</p>
<p>Mom. 8 Om en elev på skolan som inte är medlem i Viktor Rydberg Odenplans Elevkår</p>
<p>väljer att bli medlem i en kommitté i kommitténs interna medlemsregister blir personen</p>
<p>även medlem i elevkåren.</p>
<p>Mom. 9 “Om en kommitté upprättar ett internt medlemssystem åtar de sig att efterfölja</p>
<p>Viktor Rydberg Odenplans Elevkårs personuppgiftspolicy och att informera kommitténs</p>
<p>medlemmar om den. Kommittén ska kunna redovisa medlemsregistret vid förfrågan av</p>
<p>elevkårsstyrelsen.</p>
<h3>§ 5 Projektgrupper</h3>
<p>Mom 1. Beslut om uppstart av en projektgrupp fattas av styrelsen.</p>
<p>Mom 2. Minst en projektgruppsledare ska utses internt i projektgruppen genom</p>
<p>en demokratisk process, alternativt utses av styrelsen.</p>
<p>Mom 3. Vid behov ska en rekryteringsansvarig utses internt i projektgruppen via</p>
<p>en demokratisk process, alternativt utses av styrelsen.</p>
<p>Mom 4. En projektplanering ska presenteras av antingen styrelsen eller</p>
<p>projektgruppen, och antagas av både styrelsen och projektgruppen.</p>
<p>Mom 5. Beslut om avveckling av en projektgrupp fattas av styrelsen.</p>
<h3>§ 6 Redovisning och ansvar</h3>
<p>Mom. 1 Det är alltid elevkårsstyrelsen som är ytterst ansvarig för all fördelning och</p>
<p>resurser och dess användning.</p>
<p>Mom. 2 Alla som har mottagit resurser från elevkåren är skyldig att redovisa vart dessa</p>
<p>resurser gått.</p>
